// HTTP Requests

// HTTP is a client/server protocol. Requests are initiated by the client (you) and the server will respond with a 'respose'. The response could be HTML, CSS, JS files, images and other resources as well as data formatted as JSON or XML

// HTTP Methods
// GET Request          - Fetch/retrive data from the server
// POST Request         - Send data to the server
// PUT & PATCH Request  - Update data on a server
// DELETE Request       - Delete data from a server

// HTTP Status Codes
// 100 Range    - Continue
// 200 Range    - Success
// 201          - Created

// 300 Range    - Redirects
// 400 Range    - Client Error
// 500 Range    - Server Error
